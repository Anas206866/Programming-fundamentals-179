#include<stdio.h>
int main(void)
{
	// Print a pattern of stars using newline characters '\n'
    // Each '\n' moves the cursor to a new line after printing stars
	printf("*\n**\n***\n****\n*****");
	/*  
       The output will look like this:
       *
       **
       ***
       ****
       *****
    */
	return 0;
}
